public class WhileToFor {

    public static void main(String[] args) {
        
        
        for (int asterisk = 0; asterisk < 10; asterisk++) { 
        System.out.print("*");
        }
            System.out.println();
          

        for (int i = 0; i < 10; i++) {
            System.out.print(i + " ");
        }
                System.out.println();
              
       

        for (int num = 100; num <= 160; num += 20) { 
            System.out.print(num + " ");
        } 
                System.out.println();
               
        
        for (int maria = 0; maria < 5; maria++) { 
        
            System.out.println("Maria");
    }
        System.out.println();
    }
    
}
